            <p>No se trata sólo de alinear un par de dientes porque están torcidos y se ven feos. La ortodoncia va más allá y persigue corregir problemas en la mordida, la mandíbula o la cara. Aunque la obsesión de los nuevos tiempos por estar perfectos parece haber desdibujado la línea entre la necesidad de someterse a una ortodoncia y la moda de hacerlo para estar milimétricamente perfectos, el auge de las ortodoncias dentales se debe al avance en los diagnósticos y las nuevas técnicas de tratamiento, así como un incremento de los adultos con ortodoncia.</p><br>


            <h3 class="titulo">¿ Qué es la ortodoncia ? </h3> 
            <p>La ortodoncia es una rama de la odontología que estudia, previene y corrige problemas dentales como desviación en la posición de los dientes, apiñamientos o diastemas (espacios entre los dientes), la forma de las arcadas dentales o problemas de mordida, para restablecer el equilibrio entre la forma y función de la boca mejorando también la estética facial. Además, la ortodoncia se enfrenta a problemas mayores como la corrección de alteraciones en la mandíbula, el maxilar superior o incluso la cara y especialmente trastornos funcionales de la masticación. </p><br>

            <h3 class="titulo">¿ Quienes necesitan utilizar la ortodoncia ? </h3>
            <p>A rasgos generales, necesitan una ortodoncia todos aquellos que presenten maloclusión de los dientes, puesto que esta anomalía puede impedir una masticación adecuada, la correcta limpieza de los dientes -con consecuencias negativas para la salud dental, como caries, pérdida de dientes, problemas de encías o desgaste del esmalte- o, incluso, generar inseguridad o hacer que los aquejados se sientan cohibidos.Las maloclusiones o problemas de mordida pueden heredarse o ser la consecuencia de una enfermedad dental, pérdida temprana de los dientes de leche o los permanentes, un accidente o trastornos médicos, entre otras causas. Por lo tanto, puede darse en los niños, pero también estar presente en los adultos -cuando no han sido tratados durante la infancia o por problemas desarrollados a lo largo de los años, como un accidente o la pérdida de una pieza por una infección.</p><br><br>
