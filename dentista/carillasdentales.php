            <p>Las carillas estéticas dentales (o venners) son otro de los tratamientos de Estética Dental más demandados. En este caso, mediante ciertos componentes como la porcelana, se fabrican pequeñas láminas de un grosor de entre 0,8 y 1,5 milímetros que se colocan sobre la superficie de la cara externa de la dentición, camuflando el diente natural. De esta manera, se proporciona un aspecto más estético a la cavidad oral y se restaura la parte frontal de los dientes para solucionar problemas referentes a la coloración dental, alteración en la forma o disposición de los dientes e incluso cerrar la separación de estos, una afección conocida como diastema.Las carillas dentales se adhieren a los dientes gracias a un cemento adhesivo especial que permite la fijación duradera.</p><br>

            <h3 class="titulo">¿ En cuantas visitas se realiza el tratamiento ? </h3>
            <p>Ahora que ya sabes qué son las carillas dentales, es interesante que conozcas que este tratamiento se ha de realizar en más de una visita a la consulta odontológica y no es permanente. Por lo general, en el tratamiento de carillas estéticas, durante la primera visita se suele informar y estudiar el caso individual de cada paciente, realizando un diagnóstico para determinar el mejor tipo de tratamiento. Posteriormente, se muestran las diversas tonalidades y resultados al paciente, para que pueda observar cómo sería el efecto final del tratamiento.</p>
            <p>En la segunda visita se preparan los dientes para colocar las carillas, tomando medidas, fotos y moldes para que queden de la mejor manera posible.</p>
            <p>Finalmente, se colocan las carillas con una resina, de manera que el paciente no pierda la funcionalidad de su cavidad oral. Sean de composite o de porcelana, las carillas estéticas conseguirán corregir cualquier tipo de defecto en el color, tamaño y forma del diente.</p>
            <p>En cuanto a la duración de las carillas, las de porcelana son las más resistentes, que suelen durar entre 10 y 15 años. En el caso de las de composite, se mantienen entre 5 y 10 años. Además, la durabilidad también dependerá de la calidad del tratamiento de estética dental y de los cuidados posteriores del propio paciente.</p><br><br>
