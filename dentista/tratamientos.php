            <h3>¿Porqué se da la sensibilididad dental?</h3>
            <p>La sensibilidad dental se produce cuando la dentina, que se encuentra protegida por la encía, cemento radicular y el esmalte dental, pierde su protección natural y queda al descubierto.</p>
            <p>La dentina contiene miles de tubos microscópicos, llamados túbulos dentinarios, que conectan la parte exterior del diente con las terminaciones nerviosas. Cuando el diente pierde su protección debido a una retracción de la encía y/o al desgaste del esmalte dental, estos túbulos quedan abiertos al exterior, permitiendo que los estímulos externos alcancen las terminaciones nerviosas y provoquen la sensación de dolor.</p>
              
