            <p>El blanqueamiento dental es un tratamiento que se aplica a los dientes que han cambiado de color, siendo uno de los tratamiento estéticos más conservadores con el tejido dentario. Con este tratamiento se consigue cambiar el tono del color de los dientes, dejando estos más blancos, luminosos y devolviendo el brillo natural del esmalte dental. Es decir, consiste en eliminar las manchas intrínsecas y extrínsecas que producen el cambio de tonalidad en la superficie dentaria. Consumir ciertos alimentos, como café o vino, y tener ciertos hábitos como fumar pueden hacer que el esmalte que recubre y protege el diente se oscurezca. Es por ello que se hacen este tipo de tratamientos, para devolver el color natural y blanquear la dentadura</p><br>

            <h3 class="titulo">¿ Porqué cambia el color de dientes ? </h3>
            <p>Entre los motivos que producen el cambio de color del diente están los empastes de composite que presentan algunos pacientes, si el paciente ha recibido un tratamiento pulpar donde el diente pierde el “nervio” y queda desvitalizado, cuando el paciente consume alimentos con un alto nivel de tintes o es fumador. El blanqueamiento dental, como tratamiento de estética dental, consiste en aplicar un gel, que contiene un producto llamado peróxido de hidrógeno, que en una concentración del 15-30% oxigena el diente produciendo su blanqueamiento. El gel se activa con una luz fría y el tratamiento tiene una duración aproximada de una hora.</p><br>

            <h3 class="titulo"> Efectos Secundarios </h3> 
            <p>Uno de los efectos secundarios más comunes que presenta este tratamiento de estética dental es la sensibilidad dental. Depende de cada paciente, este efecto secundario puede llegar a ser más leve o más severo. Para aliviar dicha sensación, el odontólogo recomendará al paciente pastas dentales para dientes sensibles. Si la sensibilidad dental es extrema, debe suspenderse el tratamiento de estética dental por unos días.</p><br><br>
