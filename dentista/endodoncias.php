            <p>La endodoncia es un tratamiento dental conocido comúnmente para “matar el nervio”. Consiste en eliminar la parte profunda del diente cuando se encuentra lesionado o infectado. El objetivo de este tratamiento es limpiar el diente por dentro y rellenarlo de un material inerte. El tratamiento se realiza bajo anestesia local, por lo que no resulta doloroso. Si es verdad que, una vez finalizado el efecto de la anestesia, el paciente podría sentir durante los siguientes días alguna molestia a la hora de masticar. El término endodoncia también se usa como sinónimo de endodontología, una rama de la estomatología que se ocupa de las enfermedades de la pulpa dental.</p><br>

            <h3 class="titulo">¿ Porqué se realiza ? </h3>
            <p>La endodoncia está indicada cuando hay una caries muy extendida o una fractura dental que ha provocado una inflamación o muerte de la pulpa dental. El tratamiento se realiza para mantener el diente sano para que sea útil para masticar, evitando tener que extraer la pieza dental. Si este tratamiento no se realiza, puede suponer la pérdida del diente, la propagación de la infección a la sangre, celulitis facial o angina de Ludwig y ostemelitis, entre otras patologías.</p><br>

            <h3 class="titulo">¿ En qué consiste ? </h3> 
            <p>La técnica consiste en extraer el nervio, limpiar el conducto de la raíz y después sellarlo con un material termoplástico (gutapercha) y cemento u otro tipo de material de obturación. Cuando ya se ha decidido que el tratamiento adecuado sería una endodoncia, entonces se planificaría la intervención, haciendo uso de anestesia local. A continuación se realiza un agujero en la corona del diente y se accede a la pulpa para extraerla y aislar el diente del organismo. Después se procede a la limpieza de los conductos y se cierran. Después de la intervención, se procede a realizar una radiografía para verificar que todo ha ido correctamente.En la mayoría de los casos el tratamiento puede realizarse en una única sesión, aunque en algunos otros puede ser necesario realizar dos o más sesiones.</p><br><br>

