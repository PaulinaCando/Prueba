            <p>Una corona dental es un tipo de prótesis fija que se coloca en la parte externa del diente, cubriendo la estructura del resto de elementos de la prótesis. Al ir colocada sobre el diente, esta quedará fija de forma que el paciente no podrá removerla. Las coronas dentales nos permiten masticar y proteger el interior del diente; además, tienen un papel fundamental en la estética dental, mejorando el aspecto de la sonrisa. Las coronas pueden ser utilizadas de forma individual, si el fin es cubrir un diente, o bien, unidas a otras para que trabajen juntas, o para que sustituyan un diente perdido. Esto último recibiría el nombre de puente dental.</p><br>

            <h3 class="titulo">¿ Cuánto duran las Coronas Dentales ? </h3>
            <p>Por lo general, las coronas de resina o de composite, se suelen utilizar más para tratamientos provisionales, con lo que tendrán menos resistencia al desgaste y fractura y menor estabilidad del color a lo largo del tiempo, a diferencia de materiales cerámicos y circonios que son materiales más estables en el tiempo y más utilizados en tratamientos definitivos. Sin embargo, no debemos olvidar que, si queremos que los tratamientos que se nos realicen en la boca sean longevos, una correcta higiene diaria y visitas anuales o semestrales al dentista deberán ser realizados por parte de los pacientes.</p><br>

            <h3 class="titulo">¿ En qué consiste la colocación de la corona dental ? </h3> 
            <p>En la primera visita, el odontólogo aplica una anestesia para extraer las restauraciones dañadas o las caries de la zona, limar los dientes, rellenar las caries y hacer una impresión de los dientes para enviar los resultados al laboratorio, donde se fabricará la corona permanente. Finalmente se coloca una corona temporal mientras el paciente espera para la corona fija. En la segunda visita, el especialista extrae la corona temporal, ajusta la corona permanente (si es necesario, hará una radiografía para asegurarse de que tiene las medidas correctas) y coloca la corona definitiva con un cemento especial.</p><br><br>
