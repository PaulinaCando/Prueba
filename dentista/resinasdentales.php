            <p>La resina dental se utiliza para reemplazar una parte cariada o ausente de su estructura. Está compuesta de materiales de restauración del mismo color del diente. Su apariencia estética es la principal ventaja sobre otros materiales de relleno como es la amalgama de plata convencional. La resina compuesta típica está compuesta por una matriz a base de resina y un relleno inorgánico como el sílice. El relleno le da al material unas propiedades mecánicas mejoradas, como es la resistencia al desgaste y la translucidez.</p><br>

            <h3 class="titulo">¿ Cuánto tiempo dura ? </h3>
            <p>Una resina dental dura en promedio de 5 a 15 años, uno de los factores más importantes es la calidad de la resina, los cuidados que sigue el paciente tomando en cuenta el desgaste diario de la masticación y los hábitos alimenticios de cada persona, los cuales pueden provocan su ruptura, cambio de color o desgaste excesivo en caso de rechinar los dientes, también conocido como bruxismo. En el peor de los casos duran un aproximado de 5 años, la mejor calidad son las cerámicas, cuya vida útil puede alcanzar incluso los 10 o 15 años si la persona ha practicado buenos hábitos dentales.</p><br>

            <h3 class="titulo">¿ Cuándo debo cambiar una resina ? </h3> 
            <p>Estas deben de ser reemplazadas a la primera señal de filtración o caries. Una vez que esto ocurre el proceso de la caries puede empezar o ya esta presente. Estas caries solo se harán mas grandes y causarán mas problemas si no las tratas a tiempo. La falta de dolor o sensibilidad no quiere decir que el diente este sano. Muchas de las caries pequeñas serán asintomáticas. Si un diente te duele o causa sensibilidad quiere decir que la caries ya esta muy grande y puede que el nervio de tu diente este comprometido.</p><br><br>
