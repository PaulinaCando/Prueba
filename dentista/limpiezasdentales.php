            <p>La limpieza bucal es el cuidado preventivo para mantener una buena higiene de la boca. La dejadez de la dentadura puede conllevar una acumulación excesiva de placa bacteriana y sarro en la boca que pueden desembocar en enfermedades dentales. Es una técnica no invasiva y ambulatoria que no requiere ningún tipo de anestesia. Para una higiene bucal óptima y saludable, se recomienda al menos una limpieza bucal anual. En pacientes con un elevado riesgo de periodontitis o grandes acumulaciones de sarro se puede recomendar una limpieza cada 6 meses.</p><br>

            <h3 class="titulo">¿ Porqué se realiza ? </h3>
            <p>La higiene dental es muy importante para evitar la aparición de caries u otras enfermedades bucales. A pesar de los cuidados diarios, es importante realizarse una limpieza bucal profesional periódica, ya que es más exhaustiva. Si no se realiza una limpieza bucal a menudo, la placa bacteriana y el sarro se van acumulando en la boca, lo que puede provocar una gran variedad de enfermedades como: Caries dentales, Gingivitis, Periodontitis</p><br>

            <h3 class="titulo">¿ En qué consiste ? </h3> 
            <p>La limpieza bucal es un tratamiento sencillo pero su intensidad depende de la placa y del sarro acumulado. El odontólogo inicia el proceso de eliminación del sarro con una herramienta de ultrasonido. A continuación, pule el diente para limpiar las manchas y trata la placa bacteriana con líquido a presión de bicarbonato de sodio. Finalmente, aplica flúor en los dientes.</p><br><br>
