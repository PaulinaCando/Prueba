            <p>La Periodoncia es la rama de la Odontología que trata las enfermedades de las encías y del hueso que sostiene los dientes. Y es que las enfermedades de las encías como la gingivitis y periodontitis son de las causas más comunes de pérdida de dientes, y su control es fundamental. Las enfermedades más comunes tratadas por la periodoncia son la gingivitis y la periodontitis.Las causas más frecuentes de estas enfermedades están relacionadas con las bacterias que permanecen sobre los dientes y las encías, que con el tiempo pueden producir lesiones en los tejidos. Dichas patologías provocan el enrojecimiento y sangrado de las encías, de forma espontánea o durante el cepillado. Otras causas son el tabaco, la herencia genética, el embarazo y algunas enfermedades como la diabetes.</p><br>

            <h3 class="titulo">¿ Se puede prevenir la enfermedad periodontal ? </h3>
            <p>La mejor forma de prevenir la enfermedad periodontal es mantener una correcta higiene bucal, controlando así la cantidad de placa dental presente. Es necesario realizar revisiones periódicas con el especialista odontólogo para poder realizar un diagnóstico precoz de la enfermedad en caso de que aparezca.</p><br>

            <h3 class="titulo">¿ En qué consiste el tratamiento ? </h3> 
            <p>El tratamiento de la gingivitis se realiza mediante profilaxis profesional (limpieza bucal), y raspado y alisado radicular en el caso de la periodontitis. En alguna ocasión, es necesaria la realización de cirugías periodontales para limpiar las bacterias, la placa y el sarro presentes en las raíces de los dientes. El paciente al que se le detecta una gingivitis o principio de gingivitis, puede prescribir el tratamiento preventivo de realizar una profilaxis anual o cada 6 meses para eliminar a fondo la placa y bacterias que pueden desarrollar enfermedades periodontales.</p><br><br>
